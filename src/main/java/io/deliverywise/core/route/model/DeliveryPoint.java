package io.deliverywise.core.route.model;
/**
 * Модель данных точки доставки (заказа).
 * Содержит все характеристики груза и географические координаты.
 * Рекорд для точки доставки.
 * * Используется как для хранения реальной информации из 1С/форм,
 *  * так и для анонимизированных данных в процессе оптимизации.
 * id: 0 всегда резервируем под СКЛАД (Сирецькя, 28/2).
 *
 * @param id           Уникальный идентификатор точки (0 всегда зарезервирован для Склада).
 * @param clientName   Название контрагента (анонимизируется перед расчетами).
 * @param address      Физический адрес доставки.
 * @param weight       Вес груза в килограммах.
 * @param boxes        Количество грузовых мест (коробок).
 * @param pallets      Количество паллет.
 * @param isSoudal     Флаг хрупкого груза (Soudal), требующий особого размещения.
 * @param isBulky      Флаг негабаритного груза (например, шпилька 2м).
 * @param timeStart    Начало временного окна доставки (в минутах от начала дня).
 * @param timeEnd      Конец временного окна доставки.
 * @param notes         Примечания для водителя или логиста.
 * @param x            Условная координата X (0-1000).
 * @param y            Условная координата Y (0-1000).
 */
public record DeliveryPoint(
    int id,
    String clientName,
    String address,
    double weight,        // Вага из таблицы
    int boxes,           // Количество коробок (Мест)
    int pallets,         // Количество паллет
    boolean isSoudal,    // ТМ Soudal (нельзя давить)
    boolean isBulky,     // Негабарит (шпилька 2м, зонтики)
    int timeStart,       // Время "ОТ" в минутах от начала дня (напр. 480 = 08:00)
    int timeEnd,         // Время "ДО" в минутах (напр. 960 = 16:00)
    String notes,        // Примітки для водія
    int x, int y         // Координаты для расчета пути
) {}

